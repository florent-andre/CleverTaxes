package eu.ooffee.utilities.jaxb;

public class JaxbSimpleException extends Exception {

	public JaxbSimpleException() {
		super();
	}

	public JaxbSimpleException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public JaxbSimpleException(String arg0) {
		super(arg0);
	}

	public JaxbSimpleException(Throwable arg0) {
		super(arg0);
	}
	
	

}
